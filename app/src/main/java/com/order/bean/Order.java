package com.order.bean;

public class Order {
	public Integer id;
	public String title;
	public Integer img;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public Integer getImg() {
		return img;
	}

	public void setImg(Integer img) {
		this.img = img;
	}

	public void setId(Integer id) {
		this.id = id;
	}

}
